    private void listagemClientes() {
        listaClientes = new BancoCliente().searchObjects();
       
                
        tabelaModelo = (DefaultTableModel) jTable2.getModel();


        for (Cliente cliente : listaClientes) {
            tabelaModelo.addRow(new Object[]{cliente.getNome(),cliente.getEmail(),cliente.getGenero(),cliente.getUsuario().getLogin()});
        }
    }
