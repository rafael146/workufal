    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
            Sessao sessao = new BancoSessao().getObject(jLabelSessao.getText());
            String formadePagamento = jComboBoxFormaPagamento.getSelectedItem().toString();
            String cadeira = jComboBoxLetra.getSelectedItem().toString()+jComboBoxNumero.getSelectedItem().toString();
            int valor = Integer.parseInt(jFormattedTextFieldValor.getText().replace(",", ""));
            Ingresso ingresso = new Ingresso(valor, sessao, formadePagamento, usuario, cadeira);
            new BancoIngresso().saveObject(ingresso);
            JOptionPane.showMessageDialog(null, "Ingresso Vendido com Sucesso!");
    
    }//GEN-LAST:event_jButton1ActionPerformed

